package net.xinshi.discovery.search.client.util;

public class NamePair {
	private String name;
	private String value;
	public NamePair(String name,String value) {
		this.name = name;
		this.value = value;
	}
	public String getName() {
		return name;
	}
	public String getValue() {
		return value;
	}
}
